function plant(seed, water, fert, temp) {
  const root = water;
  const flower = seed;
  let plant = "";
  const isSafeTemp = temp <= 30 && temp >= 20;

  for (let w = 0; w < water; w++) {
    for (let r = 0; r < root; r++) {
      plant += "-";
    }
    if (isSafeTemp) {
      for (let f = 0; f < fert; f++) {
        plant += seed;
      }
    }
  }

  if (!isSafeTemp) {
    plant += seed;
  }

  return plant;
}

